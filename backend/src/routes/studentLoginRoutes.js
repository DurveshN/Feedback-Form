const express = require("express");
const { authenticateUser } = require("../middleware/authMiddleware");
const {
  listStudentLogins,
  addStudentLogin,
  updateStudentLogin,
  deleteStudentLogin,
} = require("../controllers/studentLoginController");

const router = express.Router();

// âœ… Protected by authentication (HOD only)
router.get("/list", authenticateUser, listStudentLogins);
